package day05_matematikselislemler;

public class C01_PreincrementPostincrement {
    public static void main(String[] args) {
        int sayi=10;
        sayi++;
        System.out.println(sayi);
        sayi++;
        System.out.println(sayi);
        System.out.println(++sayi);
        System.out.println(sayi);
        System.out.println(sayi++);
        System.out.println(sayi);
    }
}
