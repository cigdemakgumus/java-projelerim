package day18_nestedForLoop;

public class C06_WhileLoop {
    public static void main(String[] args) {

        // kullanicidan iki tamsayi alip
        // bu sayilari ve aralarindaki tum tamsayilari yazdiran bir kod olusturun

        int baslangic=40;
        int bitis=60;

        int i=baslangic;

        while (i<=bitis){
            System.out.print(i+" ");
            i++;
        }
        System.out.println("");
    }
}
